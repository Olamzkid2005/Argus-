# Bug Bounty Report Generator
# ═══════════════════════════════════════════════════════════════════════════════

class BugBountyReportGenerator:
    """
    Generates platform-specific bug bounty reports from Argus findings.

    Follows the same pattern as LLMReportGenerator and ComplianceReportGenerator.
    Uses Bug-Reaper's template system (ported) for platform formatting.

    Usage:
        generator = BugBountyReportGenerator()
        md = generator.generate(findings, platform="hackerone", engagement=engagement_data)
    """

    SUPPORTED_PLATFORMS = list(GENERATORS.keys())

    def __init__(self):
        self.adapter = ArgusFindingAdapter()

    def generate(
        self,
        findings: list[dict],
        platform: str,
        engagement: dict | None = None,
        min_confidence: float = 0.65,
    ) -> str:
        """
        Generate a platform-specific bug bounty report from Argus findings.

        Applies Bug-Reaper's audit rules:
        - Only findings with confidence >= min_confidence
        - Only findings with severity CRITICAL, HIGH, or MEDIUM
        - Excludes findings flagged as false positives

        Args:
            findings: List of Argus finding dicts (VulnerabilityFinding model)
            platform: Target platform ('hackerone', 'bugcrowd', 'intigriti', 'yeswehack')
            engagement: Optional engagement dict with metadata
            min_confidence: Minimum confidence threshold (default 0.65 = Bug-Reaper standard)

        Returns:
            Markdown string with platform-formatted report(s)
        """
        slog = ScanLogger("bugbounty_report_generator")

        platform = platform.lower()
        if platform not in self.SUPPORTED_PLATFORMS:
            raise ValueError(
                f"Unsupported platform: {platform}. "
                f"Supported: {', '.join(self.SUPPORTED_PLATFORMS)}"
            )

        slog.phase_header("Bug Bounty Report", platform)
        slog.info(f"Processing {len(findings)} findings (min_confidence={min_confidence})")

        # Apply Bug-Reaper audit rules: filter findings by quality
        reportable = self._filter_findings(findings, min_confidence)
        slog.info(f"{len(reportable)}/{len(findings)} findings reportable after filtering")

        if not reportable:
            slog.info("No reportable vulnerabilities — generating empty report")
            return (
                f"# Bug Bounty Report — {platform.upper()}\n\n"
                f"**No reportable vulnerabilities identified.**\n\n"
                f"Findings that were evaluated:\n"
                + "\n".join(
                    f"- {f.get('type', 'Unknown')} at {f.get('endpoint', '?')} "
                    f"(confidence: {f.get('confidence', 0):.2f}, "
                    f"severity: {f.get('severity', '?')}) "
                    f"— {'below confidence threshold' if f.get('confidence', 0) < min_confidence else 'below Medium severity'}"
                    for f in findings
                )
                if findings
                else "No findings found for this engagement."
            )

        # Generate platform-specific reports
        reports = []
        generator_fn = GENERATORS[platform]

        for finding in reportable:
            vuln_type = self._map_type(finding)
            finding_data = self.adapter.adapt(finding, vuln_type, engagement)
            try:
                report_md = generator_fn(finding_data, vuln_type)
                reports.append(report_md)
            except Exception as e:
                slog.warn(f"Failed to generate report for finding {finding.get('id', '?')}: {e}")
                logger.warning(f"Failed to generate report for finding {finding.get('id', '?')}: {e}")
                continue

        # Wrap with header
        header = (
            f"# Argus Bug Bounty Report — {platform.upper()}\n"
            f"**Generated:** {datetime.now().strftime('%Y-%m-%d %H:%M UTC')}\n"
            f"**Findings:** {len(reports)} reportable\n"
            f"**Target:** {engagement.get('target_url', 'N/A') if engagement else 'N/A'}\n\n"
            f"---\n\n"
        )

        slog.tool_complete("report_generation", platform=platform, reports=len(reports))
        return header + "\n\n---\n\n".join(reports)

    def _filter_findings(self, findings: list[dict], min_confidence: float) -> list[dict]:
        """
        Apply Bug-Reaper's audit rules:
        1. Minimum confidence threshold (0.65 = Probable)
        2. Minimum severity: Medium
        3. Exclude false positives
        4. Exclude unvalidated findings (requires_validation with no validation)
        """
        reportable = []
        rejected = []

        for f in findings:
            # Check false positive flag
            if f.get("false_positive") or f.get("fp_assessment", {}).get("verdict") in (
                "false_positive", "likely_false_positive"
            ):
                rejected.append(f)
                continue

            # Check severity
            severity = str(f.get("severity", "")).upper()
            if severity not in ("CRITICAL", "HIGH", "MEDIUM"):
                rejected.append(f)
                continue

            # Check confidence
            confidence = float(f.get("confidence", 0))
            if confidence < min_confidence:
                rejected.append(f)
                continue

            reportable.append(f)

        return reportable

    def _map_type(self, finding: dict) -> str:
        """Map Argus finding type to Bug-Reaper vulnerability type."""
        type_str = str(finding.get("type", "")).upper()
        mapped = ARGUS_TYPE_TO_BUGREAPER.get(type_str, "api-graphql")

        # Fallback: check tags for hints
        if mapped == "api-graphql":
            tags = finding.get("tags", [])
            for tag in tags:
                if tag in ARGUS_TYPE_TO_BUGREAPER:
                    return ARGUS_TYPE_TO_BUGREAPER[tag]

        return mapped

    def list_supported_platforms(self) -> list[str]:
        """List all supported bug bounty platforms."""
        return self.SUPPORTED_PLATFORMS

    def list_supported_types(self) -> list[str]:
        """List all supported vulnerability types."""
        return list(VULN_META.keys())


