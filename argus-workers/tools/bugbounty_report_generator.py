def main():
    """
    CLI entry point for generating bug bounty reports.

    Usage:
        python -m tools.bugbounty_report_generator \\
            --findings findings.json \\
            --platform hackerone \\
            --target-url https://example.com \\
            --output report.md
    """
    import argparse

    parser = argparse.ArgumentParser(
        description="Generate Bug Bounty report from Argus findings"
    )
    parser.add_argument("--findings", required=True,
                        help="Path to JSON file with findings array")
    parser.add_argument("--platform", required=True,
                        choices=list(GENERATORS.keys()),
                        help="Target bug bounty platform")
    parser.add_argument("--target-url", default="",
                        help="Target URL for the engagement")
    parser.add_argument("--output", default="",
                        help="Output file path (default: stdout)")
    parser.add_argument("--min-confidence", type=float, default=0.65,
                        help="Minimum confidence threshold (default: 0.65)")

    args = parser.parse_args()

    # Load findings
    findings_path = Path(args.findings)
    if not findings_path.exists():
        print(f"Error: Findings file not found: {args.findings}", file=sys.stderr)
        sys.exit(1)

    try:
        with open(findings_path) as f:
            findings = json.load(f)
    except json.JSONDecodeError as e:
        print(f"Error: Invalid JSON in findings file: {e}", file=sys.stderr)
        sys.exit(1)

    if not isinstance(findings, list):
        findings = [findings]

    # Build engagement context
    engagement = {}
    if args.target_url:
        engagement["target_url"] = args.target_url

    # Generate report
    generator = BugBountyReportGenerator()
    try:
        report_md = generator.generate(
            findings=findings,
            platform=args.platform,
            engagement=engagement,
            min_confidence=args.min_confidence,
        )
    except ValueError as e:
        print(f"Error: {e}", file=sys.stderr)
        sys.exit(1)

    # Output
    if args.output:
        output_path = Path(args.output)
        output_path.write_text(report_md, encoding="utf-8")
        print(f"Report saved to {args.output}")
        print(f"Platform: {args.platform.upper()}")
        print(f"Findings in report: {findings_count(report_md)}")
    else:
        print(report_md)


def findings_count(report_md: str) -> int:
    """Count number of individual finding reports in the markdown."""
    return max(1, report_md.count("## Summary") + report_md.count("## Technical Details"))


if __name__ == "__main__":
    main()
