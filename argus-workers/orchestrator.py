    def _check_maven_dependencies(self, repo_path: str) -> List[Dict]:
        """
        Check Maven dependencies for known vulnerabilities using pom.xml parsing.
        
        Args:
            repo_path: Path to the repository
            
        Returns:
            List of dependency vulnerability findings
        """
        findings = []
        try:
            import xml.etree.ElementTree as ET
            
            pom_files = glob.glob(os.path.join(repo_path, '**/pom.xml'), recursive=True)
            for pom_file in pom_files:
                try:
                    tree = ET.parse(pom_file)
                    root = tree.getroot()
                    
                    # Define namespace
                    ns = {'maven': 'http://maven.apache.org/POM/4.0.0'}
                    
                    # Extract dependencies
                    deps = root.findall('.//maven:dependency', ns)
                    for dep in deps:
                        group_id = dep.find('maven:groupId', ns)
                        artifact_id = dep.find('maven:artifactId', ns)
                        version = dep.find('maven:version', ns)
                        
                        if group_id is not None and artifact_id is not None:
                            finding = {
                                'type': 'DEPENDENCY_LISTING',
                                'severity': 'INFO',
                                'endpoint': f"maven:{group_id.text}:{artifact_id.text}",
                                'evidence': {
                                    'group_id': group_id.text,
                                    'artifact_id': artifact_id.text,
                                    'version': version.text if version is not None else 'unknown',
                                    'pom_file': os.path.relpath(pom_file, repo_path),
                                    'title': f"Maven dependency: {group_id.text}:{artifact_id.text}",
                                },
                                'confidence': 1.0,
                                'tool': 'maven_check',
                            }
                            findings.append(finding)
                except ET.ParseError as e:
                    logger.warning(f"Failed to parse {pom_file}: {e}")
        except Exception as e:
            logger.error(f"Maven dependency check failed: {e}")
        return findings

    def _check_timeout(self):
        """
        Check if hard timeout has been exceeded
        
        Raises:
            EngagementTimeoutError: If timeout exceeded
        """
        if self.start_time is None:
            return
        
        elapsed = time.time() - self.start_time
        
        if elapsed > HARD_TIMEOUT_SECONDS:
            # Log timeout event
            self._log_timeout_event(elapsed)
            
            raise EngagementTimeoutError(
                f"Engagement {self.engagement_id} exceeded hard timeout "
                f"of {HARD_TIMEOUT_SECONDS} seconds (elapsed: {elapsed:.2f}s)"
            )
    
    def _log_timeout_event(self, elapsed_seconds: float):
        """
        Log timeout event to execution logs
        
        Args:
            elapsed_seconds: Elapsed time in seconds
        """
        # Log timeout event
        logger.warning(
            f"Engagement {self.engagement_id} exceeded hard timeout. "
            f"Elapsed: {elapsed_seconds:.2f}s, Limit: {HARD_TIMEOUT_SECONDS}s"
        )
    
    def get_elapsed_time(self) -> float:
        """
        Get elapsed time since start
        
        Returns:
            Elapsed time in seconds
        """
        if self.start_time is None:
            return 0.0
        
        return time.time() - self.start_time
    
    def get_remaining_time(self) -> float:
        """
        Get remaining time before timeout
        
        Returns:
            Remaining time in seconds
        """
        elapsed = self.get_elapsed_time()
        return max(0.0, HARD_TIMEOUT_SECONDS - elapsed)
    
